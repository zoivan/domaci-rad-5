var godine = 3;


if (godine > 2) {
    console.log("Vi imate " + (24 + (godine - 2)*5) + " godine u psećim godinama")
}
    else if (godine === 2) {
    console.log("Vi imate 24 godine u psećim godinama")
}
else if (godine === 1) {
    console.log("Vi imate 15 godina u psećim godinama")
}
else {
    console.log("Pogrešan unos godina mora biti od 0 do beskonačno")
}
